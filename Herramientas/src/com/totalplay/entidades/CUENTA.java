package com.totalplay.entidades;

public class CUENTA {

	private String CUENTA;
	private String TIPO_CUENTA;
	private String ESTATUS_CUENTA;
	private long DID;
	private String ESTATUS_DID;
	private String DIRECCION;
	private String CP;
	
	
	public String getCUENTA() {
		return CUENTA;
	}
	public void setCUENTA(String cUENTA) {
		CUENTA = cUENTA;
	}
	public String getTIPO_CUENTA() {
		return TIPO_CUENTA;
	}
	public void setTIPO_CUENTA(String tIPO_CUENTA) {
		TIPO_CUENTA = tIPO_CUENTA;
	}
	public String getESTATUS_CUENTA() {
		return ESTATUS_CUENTA;
	}
	public void setESTATUS_CUENTA(String eSTATUS_CUENTA) {
		ESTATUS_CUENTA = eSTATUS_CUENTA;
	}
	public long getDID() {
		return DID;
	}
	public void setDID(long dID) {
		DID = dID;
	}
	public String getESTATUS_DID() {
		return ESTATUS_DID;
	}
	public void setESTATUS_DID(String eSTATUS_DID) {
		ESTATUS_DID = eSTATUS_DID;
	}
	public String getDIRECCION() {
		return DIRECCION;
	}
	public void setDIRECCION(String dIRECCION) {
		DIRECCION = dIRECCION;
	}
	public String getCP() {
		return CP;
	}
	public void setCP(String cP) {
		CP = cP;
	}
}
